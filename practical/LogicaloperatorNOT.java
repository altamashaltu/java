package corejava;

public class LogicaloperatorNOT {
	public static void main(String[] args) {
	//initialising variables
		
		int a = 10, b = 1;
		
		//displaying a,b
		System.out.println("Var1 = "+a);
		System.out.println("Var2 ="+b);
		
		//using logical NOT operator
		System.out.println("!(a<b) =" + !(a<b));
		System.out.println("!(a>b) =" + !(a>b));
		
	
	}
}
